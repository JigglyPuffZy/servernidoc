// Doc Santi Veterinary AI Assistant - Serverless Version
// 
// This project has been converted to use Vercel serverless functions.
// The chat functionality is now available as a serverless function at:
// /api/chat
//
// To deploy to Vercel:
// 1. Push this code to a GitHub repository
// 2. Connect the repository to Vercel
// 3. Vercel will automatically detect and deploy the serverless functions
//
// To test locally:
// 1. Install Vercel CLI: npm install -g vercel
// 2. Run: vercel dev
//
// The serverless function can be found in: api/chat.js
// Configuration for Vercel deployment can be found in: vercel.json